"""Промпты двухэтапного pipeline.

Этап 1 (extraction): из «сырых» файлов вытащить только явные факты, привязанные
к источнику, и зафиксировать противоречия. Никаких домыслов.

Этап 2 (synthesis): из набора фактов собрать строго структурированный документ.
Запрещено добавлять то, чего нет в фактах. Конфликты разрешаются с обоснованием.

Оба этапа требуют ответа ТОЛЬКО в JSON для воспроизводимости и парсинга.
"""
from __future__ import annotations

from typing import List

from .ingest import NormalizedFile, render_for_prompt


# Категории фактов соответствуют полям итогового документа.
FACT_CATEGORIES = [
    "project_overview",
    "goals",
    "requirements",
    "technical_solution",
    "architecture",
    "team",
    "timeline",
    "budget",
    "risks",
]


EXTRACTION_SYSTEM = (
    "Ты — аккуратный аналитик. Извлекаешь из разрозненных материалов проекта "
    "ТОЛЬКО факты, которые явно присутствуют в тексте. Запрещено домысливать, "
    "обобщать сверх написанного или добавлять типичные для подобных проектов "
    "сведения. Каждый факт привязывается к файлу-источнику. Если в тексте есть "
    "взаимоисключающие утверждения — фиксируешь это как конфликт, а не выбираешь "
    "сам. Отвечаешь СТРОГО одним JSON-объектом без пояснений и без Markdown."
)


def build_extraction_user(files: List[NormalizedFile]) -> str:
    blocks = "\n\n".join(render_for_prompt(f) for f in files)
    categories = ", ".join(FACT_CATEGORIES)
    return f"""Ниже материалы проекта, каждый в теге <file>.

{blocks}

Извлеки атомарные факты. Допустимые категории: {categories}.

Верни JSON строго такой формы:
{{
  "facts": [
    {{"category": "<одна из категорий>", "statement": "<факт своими словами, кратко>", "source": "<имя файла>"}}
  ],
  "conflicts": [
    {{"description": "<в чём противоречие>", "sources": ["<файл1>", "<файл2>"]}}
  ]
}}

Правила:
- только то, что явно есть в материалах; ничего не выдумывай;
- одна мысль — один факт;
- statement формулируй кратко и нейтрально;
- если по категории фактов нет — просто не добавляй их;
- числа, суммы, сроки, имена переноси без искажений;
- если данные противоречат друг другу — занеси в conflicts, факты всё равно укажи с их источниками.
Ответ — только JSON."""


SYNTHESIS_SYSTEM = (
    "Ты — технический писатель. Собираешь проектную документацию ИСКЛЮЧИТЕЛЬНО из "
    "переданного списка фактов. Для каждого утверждения или пункта в документе ты ОБЯЗАН "
    "в квадратных скобках указать имя файла-источника, откуда взят факт (например: [brief.md] "
    "или [brief.md, budget.json]). Категорически запрещено вносить сведения, которых нет "
    "в фактах. Если по полю фактов нет — оставляешь пустую строку или пустой список. "
    "При конфликте выбираешь наиболее обоснованный вариант и кратко поясняешь выбор в "
    "поле resolution_notes. Отвечаешь СТРОГО одним JSON-объектом."
)


def build_synthesis_user(facts_json: str) -> str:
    return f"""Факты, извлечённые из материалов проекта (JSON):

{facts_json}

Собери документ строго из этих фактов. Верни JSON ровно такой формы:
{{
  "document": {{
    "project_overview": "",
    "goals": [],
    "requirements": [],
    "technical_solution": "",
    "architecture": "",
    "team": [],
    "timeline": "",
    "budget": "",
    "risks": []
  }},
  "resolution_notes": ["<как разрешён конфликт, если были>"]
}}

Правила:
- используй ТОЛЬКО переданные факты; ничего не добавляй от себя;
- ОБЯЗАТЕЛЬНО указывай источник для каждого пункта или предложения в формате '[имя_файла]' в конце (например: '- Сократить время доставки на 25% [project_brief.md]');
- если у пункта несколько источников, укажи их все в одних скобках через запятую (например: '[project_brief.md, meeting_notes.md]');
- если по полю нет ни одного факта — оставь "" или [];
- списки (goals, requirements, team, risks) — это массивы коротких строк;
- текстовые поля — связный, но сжатый текст без воды;
- не дублируй один и тот же пункт;
- суммы, сроки, имена сохраняй точно как в фактах;
- если фактов из conflicts несколько — выбери один и поясни в resolution_notes.
Ответ — только JSON."""
